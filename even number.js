// evenNumbers.js
// Print numbers from 1 to 10
console.log("All numbers from 1 to 10:");
for (let i = 1; i <= 10; i++) {
    console.log(i);
}

// Print only even numbers from 1 to 10
console.log("Even numbers from 1 to 10:");
for (let i = 1; i <= 10; i++) {
    if (i % 2 === 0) {
        console.log(i);
    }
}

